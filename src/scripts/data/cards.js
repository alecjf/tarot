function updateCards(cards) {
	return [
		...cards.map((card) => ({
			...card,
			words: [
				...new Set(card.words.map((word) => nounify(word)).flat()),
			].sort(),
		})),
		...cards.map((card) => ({
			name: card.name + " reversed",
			words: reverseWords(card.words),
		})),
	];
}

